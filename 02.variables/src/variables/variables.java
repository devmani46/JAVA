package variables;

public class variables {

	public static void main(String[] args) {
		
		int x = 123;
		long a = 123123123123L;
		double b = 123.1231;
		boolean c = false;
		char symbol = '@';
		String name = "My name";
		
		System.out.println(x);
		System.out.println("My number is : "+x);
		System.out.println("This is a long integer : "+a);
		System.out.println("This is a float integer : "+b);
		System.out.println("This is a boolean : "+c);
		System.out.println("This is a char : "+symbol);
		System.out.println("This is a string : "+name);
	}

}
